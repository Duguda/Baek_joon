package StudySem;

public class scratch
{
    public int sum(int f, int s)
    {
        return f+s;
    }

    public int min(int f, int s)
    {
        return f-s;
    }

    public int tim(int f, int s)
    {
        return f*s;
    }

    public double div(double f, double s)
    {
        if (s == 0)
            return -1;
        return f/s;
    }
}
